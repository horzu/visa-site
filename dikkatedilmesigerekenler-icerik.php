<!-- blog font size adjust -->
<script>
    function fontsizeUp() {
        let fontSize = parseInt($(".blog-detail-body").css("font-size"));
        if (fontSize < 26) {
            fontSize = fontSize + 1 + "px";
            $('.blog-detail-body').css({
                'font-size': fontSize
            });
        }
    }

    function fontsizeDown() {
        let fontSize = parseInt($(".blog-detail-body").css("font-size"));
        if (fontSize > 12) {
            fontSize = fontSize - 1 + "px";
            $('.blog-detail-body').css({
                'font-size': fontSize
            });
        }
    }
</script>

<!-- Load Sidecard Blogs with ajax -->
<script>
    $(document).ready(
        $(function() {
            $.ajax({
                url: "services/thingtoconsider.json",
                type: "GET",
                dataType: "json",
                success: sidecardBlog,
                error: function() {
                    console.log("Error in the request");
                },
            });
        })
    );

    function sidecardBlog(data) {
        $.each(data, function(key, value) {
            if (key < 4) {
                $(".sidecards").prepend(
                    '<div class="card border-0 d-flex flex-row">' +
                    '<img src="' + value["things_img"] + '" class="card-img-top" alt="...">' +
                    '<div class="card-body">' +
                    '<p class="card-text">' +
                    '<a href="" class="link-orange">' + value["things_category"] + '</a>' +
                    '</p>' +
                    '<h5 class="card-title">' +
                    '<a href="' + value["things_url"] + '">' +
                    value["things_title"] + '</a>' +
                    '</h5>' +
                    '</div>' +
                    '</div>'
                );
            }
        });
    }
</script>

